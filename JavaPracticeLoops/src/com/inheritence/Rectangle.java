package com.inheritence;

public class Rectangle extends Shape {
	
	
	private int length;
	private int width;
	
	//Getter Setter Method for Rectangle
	
	public void setLength(int length) {
		this.length = length;
		
	}
	
	public int getLength() {
		return length;
	}
	
	public void setWidth(int width) {
		this.width = width;
		
	}
	
	public int getWidth() {
		return width;
	

}
	
}
