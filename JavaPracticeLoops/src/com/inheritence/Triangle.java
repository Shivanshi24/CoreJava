package com.inheritence;

public class Triangle extends Shape {
	
	private int base;
	private int height;
	
	//Getter Setter Method for Triangle

	public void setBase(int base) {
		this.base = base;

	}

	public int getBase() {
		return base;

	}
	
	public void setHeight(int height) {
		this.height = height;

	}

	public int getHeight() {
		return height;

	}


}
